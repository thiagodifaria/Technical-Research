#include <gtest/gtest.h>
#include "problemas_backtracking_recursao/resolvedor_sudoku.hpp"

// Suíte de testes para o Resolvedor de Sudoku
TEST(SudokuSolverTest, TesteTabuleiroValido) {
    TabuleiroSudoku tabuleiro = {
        {5, 3, 0, 0, 7, 0, 0, 0, 0},
        {6, 0, 0, 1, 9, 5, 0, 0, 0},
        {0, 9, 8, 0, 0, 0, 0, 6, 0},
        {8, 0, 0, 0, 6, 0, 0, 0, 3},
        {4, 0, 0, 8, 0, 3, 0, 0, 1},
        {7, 0, 0, 0, 2, 0, 0, 0, 6},
        {0, 6, 0, 0, 0, 0, 2, 8, 0},
        {0, 0, 0, 4, 1, 9, 0, 0, 5},
        {0, 0, 0, 0, 8, 0, 0, 7, 9}
    };

    TabuleiroSudoku solucao_esperada = {
        {5, 3, 4, 6, 7, 8, 9, 1, 2},
        {6, 7, 2, 1, 9, 5, 3, 4, 8},
        {1, 9, 8, 3, 4, 2, 5, 6, 7},
        {8, 5, 9, 7, 6, 1, 4, 2, 3},
        {4, 2, 6, 8, 5, 3, 7, 9, 1},
        {7, 1, 3, 9, 2, 4, 8, 5, 6},
        {9, 6, 1, 5, 3, 7, 2, 8, 4},
        {2, 8, 7, 4, 1, 9, 6, 3, 5},
        {3, 4, 5, 2, 8, 6, 1, 7, 9}
    };

    EXPECT_TRUE(resolver_sudoku(tabuleiro));
    EXPECT_EQ(tabuleiro, solucao_esperada);
}

TEST(SudokuSolverTest, TesteTabuleiroInsoluvel) {
    // Tabuleiro inválido desde o início (dois 5s na primeira linha)
    TabuleiroSudoku tabuleiro = {
        {5, 3, 0, 0, 7, 0, 0, 0, 5},
        {6, 0, 0, 1, 9, 5, 0, 0, 0},
        {0, 9, 8, 0, 0, 0, 0, 6, 0},
        {8, 0, 0, 0, 6, 0, 0, 0, 3},
        {4, 0, 0, 8, 0, 3, 0, 0, 1},
        {7, 0, 0, 0, 2, 0, 0, 0, 6},
        {0, 6, 0, 0, 0, 0, 2, 8, 0},
        {0, 0, 0, 4, 1, 9, 0, 0, 5},
        {0, 0, 0, 0, 8, 0, 0, 7, 9}
    };
    
    // Cria uma cópia para verificar se o tabuleiro não foi alterado permanentemente
    TabuleiroSudoku tabuleiro_original = tabuleiro;
    EXPECT_FALSE(resolver_sudoku(tabuleiro));
    
    // Um bom resolvedor deve retornar o tabuleiro ao seu estado original em caso de falha.
    EXPECT_EQ(tabuleiro, tabuleiro_original);
}

TEST(SudokuSolverTest, TesteTabuleiroJaResolvido) {
    TabuleiroSudoku solucao = {
        {5, 3, 4, 6, 7, 8, 9, 1, 2},
        {6, 7, 2, 1, 9, 5, 3, 4, 8},
        {1, 9, 8, 3, 4, 2, 5, 6, 7},
        {8, 5, 9, 7, 6, 1, 4, 2, 3},
        {4, 2, 6, 8, 5, 3, 7, 9, 1},
        {7, 1, 3, 9, 2, 4, 8, 5, 6},
        {9, 6, 1, 5, 3, 7, 2, 8, 4},
        {2, 8, 7, 4, 1, 9, 6, 3, 5},
        {3, 4, 5, 2, 8, 6, 1, 7, 9}
    };
    
    TabuleiroSudoku copia_solucao = solucao;
    EXPECT_TRUE(resolver_sudoku(copia_solucao));
    EXPECT_EQ(copia_solucao, solucao); // Garante que não foi alterado
}